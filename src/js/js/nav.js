$.ajax({
    type: "post",
    url: "/index/floor-107.do",
    dataType: "json",
    data: {
        wd: ""
    },
    jsonp: "callback",
    success: function (data) {
        //data = JSON.parse(data);
        //console.log(data);
        //console.log(data.data.floorTitle)
        var str = "";
        var inner = $(".one-left");
        str += `
                <li class="sub_m_1">
                    <i><img src=""></i>
                    <a href="" target="_blank">${data.data.floorTitle}</a>
                    <b></b>
                </li>
            `
        inner.append(str)
    },
    error: function (e) {
        console.log(e)
    }
})

